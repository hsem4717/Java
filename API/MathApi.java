package API;

import java.lang.Math;

public class MathApi {

	public static void main(String[] args) {
		System.out.printf("파이: %f\n", Math.PI);
		System.out.printf("랜덤값: %f\n", Math.random());
		System.out.printf("내림: %.2f\n", Math.floor(9.81));
		System.out.printf("2의 제곱: %.2f\n", Math.sqrt(2));
		System.out.printf("2의 3승: %.2f\n", Math.pow(2, 3));
		
		

	}

}
